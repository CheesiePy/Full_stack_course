// Timing Events


// 1. setTimeout() - calls a function after a specified number of milliseconds

function f(){
    console.log("Hello");
}

setTimeout(f, 3000); // 3 seconds


// 2. setInterval() - calls a function repeatedly, after a specified number of milliseconds

setInterval(f, 3000); // 3 seconds


